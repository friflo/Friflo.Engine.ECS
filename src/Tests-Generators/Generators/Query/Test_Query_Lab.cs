// ReSharper disable InconsistentNaming
partial class TestClass
{
    [Query]
    [AllComponents<MyComponent1>]
    [AnyComponents<MyComponent2>]
    [WithoutAllComponents<MyComponent3>]
    [WithoutAnyComponents<MyComponent4>]
    [AllTags<TestTag>]
    [AnyTags<TestTag2>]
    [WithoutAllTags<TestTag3>]
    [WithoutAnyTags<TestTag4>]
    private static void MovePosition(ref Position position, float deltaTime) {
        position.x += deltaTime;
    }
    
#region will be generated
    
    /// <summary>Query method generated by <see cref="MovePosition"/>.</summary>
    /// <returns>The executed <see cref="ArchetypeQuery"/> for debugging purposes</returns>
    public static ArchetypeQuery MovePositionQuery(EntityStore store, float deltaTime, bool vectorized)
    {
        var query = _MovePosition_GetQuery(store);
        foreach (var chunk in query.Chunks)
        {
            var entities = chunk.Entities;
            var positionSpan = chunk.Chunk1.Span;
            int n = 0;
            if (!vectorized) goto EntityLoop;
            if (Avx.IsSupported) {
                n = _MovePosition_Avx(positionSpan, deltaTime);
            }
        EntityLoop:
            for (; n < entities.Length; n++) {
                MovePosition(ref positionSpan[n], deltaTime);
            }
        }
        return query;
    }
    
    private static readonly int _MovePosition_Slot = EntityStore.UserDataNewSlot();
    
    private static ArchetypeQuery<Position> _MovePosition_GetQuery(EntityStore store)
    {
        var query = (ArchetypeQuery<Position>)EntityStore.UserDataGet(store, _MovePosition_Slot);
        if (query != null) {
            return query;
        }
        query = store.Query<Position>();
        query.AllComponents(ComponentTypes.Get<MyComponent1>());
        
        EntityStore.UserDataSet(store, _MovePosition_Slot, query);
        return query;
    }
    
    private static unsafe int _MovePosition_Avx(Span<Position> position, float deltaTime)
    {
        var deltaTime_scalar = Vector256.Create(deltaTime);
        int i = 0;
        var end = position.Length - 8;
        fixed (Position* position_first = position)
        {
            // We iterate in steps of 8 (8 * Vector3 = 24 floats = 3 * 256-bit registers)
            for (; i <= end; i += 8)
            {
                float* position_ptr = (float*)(position_first + i);

                // 1. LOAD: 3 registers filled with interleaved XYZ data
                Vector256<float> position_0 = Avx.LoadVector256(position_ptr);      // [X0 Y0 Z0 X1 Y1 Z1 X2 Y2]
                Vector256<float> position_1 = Avx.LoadVector256(position_ptr + 8);  // [Z2 X3 Y3 Z3 X4 Y4 Z4 X5]
                Vector256<float> position_2 = Avx.LoadVector256(position_ptr + 16); // [Y5 Z5 X6 Y6 Z6 X7 Y7 Z7]
                
                // 2. COMPUTE: Directly in the interleaved state!
                position_0 = Avx.Add(position_0, deltaTime_scalar);
                position_1 = Avx.Add(position_1, deltaTime_scalar);
                position_2 = Avx.Add(position_2, deltaTime_scalar);

                // 3. STORE: 3 fast block writes
                Avx.Store(position_ptr, position_0);
                Avx.Store(position_ptr + 8, position_1);
                Avx.Store(position_ptr + 16, position_2);
            }
        }
        return i;
    }
    #endregion
}


